    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if (txtKodeJenis.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Pilih mana yang akan dihapus ?");
        } else {
            int jawab = JOptionPane.showConfirmDialog(rootPane, "Hapus Jenis CD " + txtKodeJenis.getText() + "?",
                    "Konfirmasi ", JOptionPane.YES_NO_OPTION);
            if (jawab == JOptionPane.YES_OPTION) {
                JenisManager jmn = new JenisManager();
                if (jmn.hapusdata(txtKodeJenis.getText()) == 1) {
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                    bersih();
                    txtKodeJenis.requestFocus();
                    tampilData();
                } else {
                    JOptionPane.showMessageDialog(this, "Data gagal dihapus");
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:

        if (txtKodeJenis.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diubah : ");
        } else {
            int jawab = JOptionPane.showConfirmDialog(rootPane, "Ubah CD Rental " + txtKodeJenis.getText() + " ?",
                    "Konfirmasi ", JOptionPane.YES_NO_OPTION);
            if (jawab == JOptionPane.YES_OPTION) {
                Jenis jn = new Jenis(txtKodeJenis.getText(), txtNamaJenis.getText());
                JenisManager jmn = new JenisManager();

                int i = jmn.ubahdata(jn);
                if (i == 1) {
                    JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                    bersih();
                    txtKodeJenis.requestFocus();
                    tampilData();
                } else {
                    JOptionPane.showMessageDialog(this, "Data gagal diubah");
                }
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

