    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            Peminjaman pjm = new Peminjaman();
            pjm.setID_member(txtIDmember.getText());
            pjm.setJumlah(txtJumlah.getText());
            pjm.setKode_cd(txtKodeCD.getText());
            pjm.setKode_jenis(txtKodeJenis.getText());
            pjm.setNo_peminjaman(txtNoPinjam.getText());
            pjm.setTgl_pinjam(txtTglPinjam.getText());
            pjm.setHarga(txtHarga.getText());
            pjm.setTotal(txtTotal.getText());

            Peminjaman pjm2 = new Peminjaman(pjm.getNo_peminjaman(), pjm.getID_member(), pjm.getKode_jenis(), pjm.getKode_cd(), pjm.getTgl_pinjam(), pjm.getHarga(), pjm.getJumlah(), pjm.getTotal());
            PeminjamanManager pjmm = new PeminjamanManager();

            int i = 0;
            i = pjmm.inputdata(pjm2);
            if (i == 1) {
                JOptionPane.showMessageDialog(null, "Simpan data Berhasil");
                tampildata();
                txtKodeCD.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Data gagal disimpan");
                txtKodeCD.requestFocus();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_btnSimpanActionPerformed

