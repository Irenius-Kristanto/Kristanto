    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if (txtNoPinjam.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diubah");
        } else {
            int jawab = JOptionPane.showConfirmDialog(rootPane, "Ubah Data Peminjaman = " + txtNoPinjam.getText() + "?",
                    "Konfirmasi", JOptionPane.YES_NO_OPTION);

            if (jawab == JOptionPane.YES_OPTION) {
                Peminjaman pinjam = new Peminjaman(txtNoPinjam.getText(), txtIDmember.getText(), txtKodeJenis.getText(), txtKodeCD.getText(),
                        txtTglPinjam.getText(), txtHarga.getText(), txtJumlah.getText(), txtTotal.getText());
                
                PeminjamanManager pm = new PeminjamanManager();

                int i = pm.ubahdata(pinjam);
                if (i == 1) {
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah");
                    bersih();
                    txtIDmember.requestFocus();
                    tampildata();
                } else {
                    JOptionPane.showMessageDialog(null, "Data gagal diubah");
                }
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (txtNoPinjam.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus");

        } else {
            int jawab = JOptionPane.showConfirmDialog(rootPane, "Hapus Data Peminjaman = " + txtNoPinjam.getText() + "?",
                    "Konfirmasi", JOptionPane.YES_NO_OPTION);

            if (jawab == JOptionPane.YES_OPTION) {
                PeminjamanManager pm = new PeminjamanManager();
                if (pm.hapusdata(txtNoPinjam.getText()) == 1) {
                    JOptionPane.showMessageDialog(this, "Berhasil dihapus");
                    bersih();
                    txtNoPinjam.requestFocus();
                    tampildata();

                } else {
                    JOptionPane.showMessageDialog(this, "Gagal dihapus");
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

