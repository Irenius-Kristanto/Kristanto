    private void Export() {
        try {
            Connection conn = kon.getConnection();
            try (Statement state = conn.createStatement()) {
                String query = "SELECT * FROM JENIS";
                try (ResultSet rs = state.executeQuery(query)) {
                    FileWriter xcl = new FileWriter("DataJenis.csv");
                    xcl.write("KODE_JENIS");
                    xcl.append(",");
                    xcl.write("NAMA_JENIS");
                    xcl.write("\n");
                    while (rs.next()) {
                        xcl.write(rs.getString("KODE_JENIS"));
                        xcl.append(",");
                        xcl.write(rs.getString("NAMA_JENIS"));
                        xcl.write("\n");
                    }
                    xcl.flush();
                    xcl.close();
                    JOptionPane.showMessageDialog(null, "Data berhasil diExport", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                    tampilData();
                }
            }
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }
