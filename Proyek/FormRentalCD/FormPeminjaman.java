    private void Export() {
        try {
            Connection conn = kon.getConnection();
            try (Statement state = conn.createStatement()) {
                String query = "SELECT * FROM PEMINJAMAN ";
                try (ResultSet rs = state.executeQuery(query)) {
                    FileWriter xcl = new FileWriter("DataPeminjaman.csv");
                    xcl.write("NO_PEMINJAMAN");
                    xcl.append(",");
                    xcl.write("ID_MEMBER");
                    xcl.append(",");
                    xcl.write("KODE_JENIS");
                    xcl.append(",");
                    xcl.write("KODE_CD");
                    xcl.append(",");
                    xcl.write("TGL_PINJAM");
                    xcl.append(",");
                    xcl.write("HARGA");
                    xcl.append(",");
                    xcl.write("JUMLAH");
                    xcl.append(",");
                    xcl.write("TOTAL");
                    xcl.write("\n");
                    while (rs.next()) {
                        xcl.write(rs.getString("NO_PEMINJAMAN"));
                        xcl.append(",");
                        xcl.write(rs.getString("ID_MEMBER"));
                        xcl.append(",");
                        xcl.write(rs.getString("KODE_JENIS"));
                        xcl.append(",");
                        xcl.write(rs.getString("KODE_CD"));
                        xcl.append(",");
                        xcl.write(rs.getString("TGL_PINJAM"));
                        xcl.append(",");
                        xcl.write(rs.getString("HARGA"));
                        xcl.append(",");
                        xcl.write(rs.getString("JUMLAH"));
                        xcl.append(",");
                        xcl.write(rs.getString("TOTAL"));
                        xcl.write("\n");
                    }
                    xcl.flush();
                    xcl.close();
                    JOptionPane.showMessageDialog(null, "Data berhasil diExport", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                    tampildata();
                }
            }
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }
