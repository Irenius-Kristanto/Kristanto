    private void tampilData() {
        DefaultTableModel tabelnya = new DefaultTableModel();
        tabelnya.addColumn("KODE JENIS CD");
        tabelnya.addColumn("NAMA JENIS CD");

        try {
            con = kon.getConnection();
            cn = con.createStatement();
            String sql = "select * from jenis order by kode_jenis asc";
            ResultSet rs = cn.executeQuery(sql);
            while (rs.next()) {
                tabelnya.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),});
            }
            tabelJenis.setModel(tabelnya);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ada Kesalahan !");
        }
    }

    private void carijenis() {
        DefaultTableModel tabelnya = new DefaultTableModel();
        tabelnya.addColumn("KODE JENIS CD");
        tabelnya.addColumn("NAMA JENIS CD");

        try {
            con = kon.getConnection();
            cn = con.createStatement();
            String sql = "select * from jenis where nama_jenis like '%" + txtCariJenis.getText() + "%'"
                    + "order by kode_jenis asc";
            ResultSet rs = cn.executeQuery(sql);
            while (rs.next()) {
                tabelnya.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),});
            }
            tabelJenis.setModel(tabelnya);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ada Kesalahan !");
        }
    }

