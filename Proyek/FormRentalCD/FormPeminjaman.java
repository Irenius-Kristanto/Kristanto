    private void caripeminjaman() {
        DefaultTableModel tbl2 = new DefaultTableModel();
        tbl2.addColumn("No Peminjaman");
        tbl2.addColumn("ID Member");
        tbl2.addColumn("Kode Jenis CD");
        tbl2.addColumn("Tgl Pinjam");
        tbl2.addColumn("Harga");
        tbl2.addColumn("Jumlah");
        tbl2.addColumn("Total");
        try {
            conn = kon.getConnection();
            stmt = conn.createStatement();
            String sql = "select * from peminjaman where kode_jenis like '%" + txtCariData.getText() + "%' "
                    + "order by no_peminjaman asc";
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                tbl2.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7),});
            }
            tabelPeminjaman.setModel(tbl2);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ada Kesalahan");
        }
    }

